def sum_mixed_list(mxd_lst: List[Union[int, float]]) -> float:
    """
    sums a mixed list of int and float numbers
    Args:
        mxd_list(list): A list of int and float numbers in union
    Returns:
        float: The sum of the int and floats in the list
    """
    return sum(mxd_lst)
